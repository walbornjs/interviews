// Число в JavaScript представлено в виде 64-бит, что равно 8 байтам.
// Сколько памяти займет данный код?

let a = 7 // 8 байт
let b = a // ? байт

/*
### Ответ:
**Общий объем памяти: 16 байт**  
(по **8 байт** для каждой переменной `a` и `b`).

### Краткое объяснение:
1. **Числа в JavaScript** всегда занимают **8 байт** (64 бита) согласно стандарту IEEE 754 для чисел двойной точности.
2. **Примитивные типы** (включая числа) хранятся **по значению**. Когда выполняется присваивание `let b = a`:
   - Значение переменной `a` (число `7`) копируется.
   - Создается **новый** 64-битный участок памяти для переменной `b`.
3. **Итог**:  
   - `a` → 8 байт,  
   - `b` → 8 байт,  
   - Итого **16 байт**.

### Почему не разделяют память?
JavaScript не использует ссылочную модель для примитивов. Каждая переменная хранит **собственную копию** значения, что гарантирует независимость изменений (например, если позже изменить `b`, это не повлияет на `a`).
*/